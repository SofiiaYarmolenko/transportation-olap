DROP TABLE IF EXISTS VEHICLE_TYPE, VEHICLE, USER_INFORMATION, TRIP_STATUS, TRIP_INFORMATION, DRIVER, TRANSPORTATION_FACTS, DIM_DATE_INFO, DIM_DRIVER,
    DIM_LOCATION, DIM_VEHICLE;

DROP SEQUENCE IF EXISTS trip_information_seq;

-- Operation data
CREATE TABLE VEHICLE_TYPE
(
    VEHICLE_TYPE_ID INT PRIMARY KEY,
    VEHICLE_TYPE    VARCHAR(50),
    DESCRIPTION     VARCHAR
);

CREATE TABLE VEHICLE
(
    VEHICLE_ID INT PRIMARY KEY,
    NUMBER_ID  VARCHAR(50),
    START_DATE DATE,
    TYPE_ID    INT
);

ALTER TABLE VEHICLE
    ADD CONSTRAINT FK_VEHICLE_TYPE
        FOREIGN KEY (TYPE_ID)
            REFERENCES VEHICLE_TYPE (VEHICLE_TYPE_ID);

CREATE TABLE USER_INFORMATION
(
    USER_ID    INT PRIMARY KEY,
    NAME       VARCHAR(50),
    SURNAME    VARCHAR(50),
    BIRTH_DATE DATE
);

CREATE TABLE DRIVER
(
    DRIVER_ID      INT PRIMARY KEY,
    USER_ID        INT,
    LICENCE_NUMBER VARCHAR(50)
);

ALTER TABLE DRIVER
    ADD CONSTRAINT FK_DRIVER_USER
        FOREIGN KEY (USER_ID)
            REFERENCES USER_INFORMATION (USER_ID);

CREATE TABLE TRIP_STATUS
(
    TRIP_STATUS_ID INT PRIMARY KEY,
    STATUS         VARCHAR(50),
    DESCRIPTION    VARCHAR
);

CREATE SEQUENCE trip_information_SEQ minvalue 500;

CREATE TABLE TRIP_INFORMATION
(
    TRIP_ID           INT PRIMARY KEY,
    DRIVER_ID         INT,
    START_COORDINATES VARCHAR(50),
    END_COORDINATES   VARCHAR(50),
    START_DATE        TIMESTAMP,
    END_DATE          TIMESTAMP,
    STATUS_ID         INT,
    VEHICLE_ID        INT,
    LAST_UPDATE_DATE  TIMESTAMP DEFAULT NOW()
);

ALTER TABLE TRIP_INFORMATION
    ADD CONSTRAINT FK_TRIP_STATUS
        FOREIGN KEY (STATUS_ID)
            REFERENCES TRIP_STATUS (TRIP_STATUS_ID);

ALTER TABLE TRIP_INFORMATION
    ADD CONSTRAINT FK_DRIVER
        FOREIGN KEY (DRIVER_ID)
            REFERENCES DRIVER (DRIVER_ID);

ALTER TABLE TRIP_INFORMATION
    ADD CONSTRAINT FK_VEHICLE
        FOREIGN KEY (VEHICLE_ID)
            REFERENCES VEHICLE (VEHICLE_ID);

-- OLAP cube
CREATE TABLE DIM_DATE_INFO
(
    DATE_INFO_ID  INT PRIMARY KEY,
    DAY_OF_MONTH  VARCHAR(10),
    MONTH_OF_YEAR VARCHAR(30),
    EXACT_YEAR    VARCHAR(10)
);

CREATE TABLE DIM_VEHICLE
(
    VEHICLE_ID INT PRIMARY KEY,
    TYPE       VARCHAR(50)
);

CREATE TABLE DIM_DRIVER
(
    DRIVER_ID INT PRIMARY KEY,
    NAME      VARCHAR(50)
);

CREATE TABLE DIM_LOCATION
(
    LOCATION_ID INT PRIMARY KEY,
    CITY        VARCHAR(100),
    COUNTRY     VARCHAR(100),
    REGION      VARCHAR(100)
);

CREATE TABLE TRANSPORTATION_FACTS
(
    FACT_ID          INT PRIMARY KEY,
    START_DATE_ID    INT,
    VEHICLE_ID       INT,
    DRIVER_ID        INT,
    FROM_LOCATION_ID INT,
    TO_LOCATION_ID   INT,
    DISTANCE         NUMERIC,
    COST             NUMERIC,
    STATUS           VARCHAR(20),
    DURATION         INT,
    TRIP_ID          INT,
    FOREIGN KEY (START_DATE_ID) REFERENCES DIM_DATE_INFO (DATE_INFO_ID),
    FOREIGN KEY (VEHICLE_ID) REFERENCES DIM_VEHICLE (VEHICLE_ID),
    FOREIGN KEY (DRIVER_ID) REFERENCES DIM_DRIVER (DRIVER_ID),
    FOREIGN KEY (FROM_LOCATION_ID) REFERENCES DIM_LOCATION (LOCATION_ID),
    FOREIGN KEY (TO_LOCATION_ID) REFERENCES DIM_LOCATION (LOCATION_ID),
    FOREIGN KEY (TRIP_ID) REFERENCES TRIP_INFORMATION (TRIP_ID)
);


